# Conclusão (12 funçoes com duas opcoes cada, totalizando 24 opçoes.)

def fc1():
    while True:
        n = int(input('Menu 3 escolha a opção : \n 1 -  \n 2 - \n' ))
        if 0 < n <= 3:
            break
        print('Opção inválida.')
    match n:
        case 1:
            print('opcoes 1 >> 1 >> 1')

        case 2:
            print('opcoes 1 >> 1 >> 2')

        case _:
            print('Valor inválido.')


